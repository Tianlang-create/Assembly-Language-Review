ASSUME CS:CODE,DS:DATA
DATA SEGMENT 
	ARRAY DW 111
	STR1 DB 'YES$'
	STR2 DB 'NO$'
DATA ENDS
CODE SEGMENT
START:
	;暴力自增
	MOV AX,DATA
	MOV DS,AX
	MOV BX,2
	MOV AX,ARRAY
ROTATE:
	MOV DX,0
	DIV BX
	CMP DX,0
	JE FALSETYPE
	MOV AX,ARRAY
	INC BX
	CMP BX,AX
	JL ROTATE
	
TRUETYPE:
	LEA DX,STR1
	MOV AH,09H
	INT 21H
	MOV AX,4C00H
	INT 21H
	
FALSETYPE:
	LEA DX,STR2
	MOV AH,09H
	INT 21H
	MOV AX,4C00H
	INT 21H
	
CODE ENDS
END START