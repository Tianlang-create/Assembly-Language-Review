;判定元素奇偶个数大小
;DATE 2024/12/26 MADE BY TL
ASSUME CS:CODE,DS:DATA
DATA SEGMENT
	ARRAY DW 11,91,82,14,23,1321,56,49,211,1314,985
	STR1 DB 'YES$'
	STR2 DB 'NO$'
DATA ENDS
CODE SEGMENT
START:
	;设计思路：判定结果存在BX中，BH存储奇数个数，BL存储偶数个数
	MOV AX,DATA
	MOV DS,AX
	LEA SI,ARRAY
	MOV BX,0
	MOV CX,11
ROTATE:
	MOV AX,[SI]
	SHR AX,1
	JC JISHU
	INC BL
	JMP CONTINUE
JISHU:
	INC BH
CONTINUE:
	ADD SI,2
    LOOP ROTATE
	CMP BL,BH
	JG YES
	LEA DX,STR2
	MOV AH,09H
	INT 21H
	MOV AX,4C00H
	INT 21H
YES:
	LEA DX,STR1
	MOV AH,09H
	INT 21H
	MOV AX,4C00H
	INT 21H
CODE ENDS
END START
	