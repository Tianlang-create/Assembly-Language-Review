;字符串的比较
;DATE 2023/12/26 MADE BY TL
ASSUME CS:CODE,DS:DATA
DATA SEGMENT
	SOURCE DB 'Hello World!'
	TARGET DB 'HeHeo World!'
	STR1 DB 'YES$'
	STR2 DB 'NO$'
DATA ENDS
CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	LEA SI,SOURCE
	LEA DI,TARGET
	MOV CX,12
ROTATE:
	MOV AL,[SI]
	CMP [DI],AL
	JE CONINUE
	JMP OUTFALSE
	CONINUE:
	INC SI
	INC DI
	LOOP ROTATE
	LEA DX,STR1
	MOV AH,09H
	INT 21H
	MOV AX,4C00H
	INT 21H
OUTFALSE:
	LEA DX,STR2
	MOV AH,09H
	INT 21H
	MOV AX,4C00H
	INT 21H
CODE ENDS
END START
	